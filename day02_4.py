#1
# 20세 이상은 성인
# 14세 이상은 청소년
# 14세 미만은 어린이

나이 = int(input('나이='))
if 나이 >= 20:
    print('성인')
elif 나이 >= 14:
    print('청소년')
elif 나이 < 14:
    print('어린이')


